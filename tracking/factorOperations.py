def eliminateWithCallTracking(callTrackingList=None):

    def eliminate(factor: Factor, eliminationVariable: str):
        #tracking check
        if not (callTrackingList is None):
            callTrackingList.append(('eliminate', eliminationVariable))
        if eliminationVariable not in factor.unconditionedVariables():
            print("Factor failed eliminate typecheck: ", factor)
            raise ValueError("Elimination variable is not an unconditioned variable " \
                            + "in this factor\n" + 
                            "eliminationVariable: " + str(eliminationVariable) + \
                            "\nunconditionedVariables:" + str(factor.unconditionedVariables()))
        if len(factor.unconditionedVariables()) == 1:
            print("Factor failed eliminate typecheck: ", factor)
            raise ValueError("Factor has only one unconditioned variable, so you " \
                    + "can't eliminate \nthat variable.\n" + \
                    "eliminationVariable:" + str(eliminationVariable) + "\n" +\
                    "unconditionedVariables: " + str(factor.unconditionedVariables()))


                    
        newUnconditionedVariables = set(factor.unconditionedVariables())
        newUnconditionedVariables.remove(eliminationVariable)
        newConditionedVariables = set(factor.conditionedVariables())

        #Create a new Factor instance for the elimination
        new_fact = Factor(newUnconditionedVariables, newConditionedVariables, factor.variableDomainsDict())
        # Iterate over all possible assignments for the new factor
        for assignment in new_fact.getAllPossibleAssignmentDicts():
            probability_sum = 0

            for value in factor.variableDomainsDict()[eliminationVariable]:
                extendedAssignment = assignment.copy()
                extendedAssignment[eliminationVariable] = value
                probability_sum += factor.getProbability(extendedAssignment)
            new_fact.setProbability(assignment, probability_sum)
        return new_fact
    return eliminate

eliminate = eliminateWithCallTracking()

